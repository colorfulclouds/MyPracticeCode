package treeset;

public class _treeset {

	private String name;
	private int score;
	public _treeset(String name,int score){
		this.name = name;
		this.score = score;
	}
	
	public String getname(){
		return name;
	}
	
	public void setname(String name){
		this.name = name;
	}
	
	public int getscore(){
		return score;
	}
	
	public void setscore(int score){
		this.score = score;
	}
}
